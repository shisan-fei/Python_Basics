'''
虚拟环境：
    在当前系统环境中，配置另一个python运行环境，可以创建多个不同运行环境
    多个环境相互独立，互不影响
优点：
    虚拟环境下，可以在没有权限的情况下安装新的库
    不同应用可以使用不同库或不同版本
    虚拟环境中库升级不受影响
    虚拟环境可以作为一个项目专有环境。在需要部署时，一键部署所需要的的包
创建虚拟环境：
    pycharm ：文件----设置---项目---python解释器---显示全部---+---虚拟环境
    linux：
        python -m venv 虚拟环境名  ：创建虚拟环境
        sourcr 虚拟环境路径
    windows:
        cd 虚拟环境
        执行activate.bat文件
    退出：
        linux
            deactivate 命令
        windows:
            直接Ctrl+c


'''
